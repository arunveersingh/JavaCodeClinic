package org.tbp.example6.problem;

public class PatientSummaryGenerator {
    // ?????????????????????????????????????????????????????????????????????
    // Problem:
    // Tight Coupling: The method generateSummary is tightly coupled with
    // the Patient class's data representation. Any change in how patient
    // data is formatted (like adding salutations to names or representing
    // age differently) requires changes in both the Patient class and the
    // PatientSummaryGenerator class.
    //
    // Violation of Single Responsibility Principle: The PatientSummaryGenerator
    // handles both the retrieval of patient data and its formatting,
    // combining two distinct responsibilities in one class.
    //
    // Lack of Flexibility: The current design isn't flexible for different
    // summary formats or future extensions, making it less adaptable to changes.
    // Imagine a way that object returns already formatted data
    // ?????????????????????????????????????????????????????????????????????
    public String generateSummary(Patient patient) {
        return "Patient: " + patient.getName() + ", Age: " + patient.getAge() +
                ", Health Condition: " + patient.getHealthCondition();
    }
}
